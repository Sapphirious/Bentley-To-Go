<h1>Questions About Your Meal Plan?</h1>
<p>We’ve kept what’s most important to you in mind, building in the convenience, flexibility, quality, value and healthy options you deserve. Explore our meal plan options to learn more about what would be the best fit for you.</p>

    <p><b>Do I need to buy a meal plan?</b></p>

    <p>All students living on-campus in university housing are required to purchase a meal plan.</p>
     
    <p><b>Will my meal plan auto renew for next semester?</b></p>

    <p>All meal plans are two-semester agreements and will auto-renew in the spring semester. If you would like to make a change to your meal plan in the spring semester, please visit 219 Ulmer.</p>

    <p><b>Will unused meals or FLEX roll over?</b></p>

    <p>If you have a block plan, you can use your meals over the course of the semester. If you have a weekly plan, you must use your meals in the week they are assigned. They will not carry over to the next week.</p>

    <p>Any unused Flex carries over from semester to semester and from year to year. However, any Flex remaining on a student’s account will be forfeited upon graduation or if the meal plan holder is not enrolled at LHU during the subsequent Fall semester.</p>

    <p>To add FLEX to your account online, click here (Student Login required), then click on the Student Accounts tab at the top. Click on the Pay My Bill or Add Flex* option in the menu on the left side of your screen.</p>

    <p>A new tab will open redirecting you to the Blackboard transact eAccounts Dining Flex page.
    You will then have the option to add FLEX with a credit card or bill it directly to your student account.</p>
    <p><b>*Please note you will be visiting a new website when you click the link.</b></p>

    <p>If I have already purchased a meal plan, can I change it?
    If you would like to make a change to your meal plan, please visit 219 Ulmer.</p>

<p>Don’t worry - simplicity and convenience are all within reach. Once you have a meal plan, your student ID works like a debit card. Just swipe your card and the meals and points are deducted from your meal plan account.</p>
